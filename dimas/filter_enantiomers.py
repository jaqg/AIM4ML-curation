import sys 
import numpy as np
import hashlib
import re

from ase.io import read, write
from ase import Atoms 

def smiles_hash(smiles):

    return int( hashlib.sha256(smiles.encode()).hexdigest(), 16) % (10**12)

def comma_string_to_dict(s):   #  We now read info as a single-entry dictionary and split 
                               #  the string (value) into fields
    items = [item.strip() for item in s.split(',')]
    return dict(zip(items[0::2], items[1::2]))

def kabsch_align(P, Q):
    """
    Align P to Q using the Kabsch algorithm.
    P and Q are (N,3) arrays of positions.
    Returns the rotated P, the rotation matrix U, and the translation vector.
    """
    # Center both sets of coordinates
    Pc = P - P.mean(axis=0)
    Qc = Q - Q.mean(axis=0)

    # Compute covariance matrix
    C = Pc.T @ Qc

    # Singular value decomposition
    V, S, Wt = np.linalg.svd(C)
    d = np.sign(np.linalg.det(V @ Wt))
    U = V @ np.diag([1, 1, d]) @ Wt

    # Rotate P to match Q
    P_rot = Pc @ U

    # Translation to match Q's centroid
    translation = Q.mean(axis=0) - P_rot.mean(axis=0)

    # Aligned
    P_aligned = P_rot + translation 

    # RMSD
    rmsd = np.sqrt(((P_aligned - Q) ** 2).mean())

    return rmsd , P_aligned

